#include <unistd.h>

int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (nb == 0)
		return (0);
	while (nb == 1)
		return (1);
	while (i <= nb / i)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
